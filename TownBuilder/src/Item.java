
public class Item {
	int curr_value;
	String category;
	String name;
	int quantity;
	int price;
	
	public Item(int curr_value, String category, String name, int quantity, int price)
	{
		this.curr_value = curr_value;
		this.category = category;
		this.name = name;
		this.quantity = quantity;
		this.price = price;
	}
}
