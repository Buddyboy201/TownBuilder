
public class Item {
	double curr_value;
	String category;
	String name;
	int quantity;
	double price;
	
	public Item(double curr_value, String category, String name, int quantity, double price)
	{
		this.curr_value = curr_value;
		this.category = category;
		this.name = name;
		this.quantity = quantity;
		this.price = price;
	}
}
